    <script>
      // Fungsi untuk mendapatkan waktu saat ini
      function getWaktuSekarang() {
        var now = new Date();
        var hours = now.getHours();
        var minutes = now.getMinutes();
        var seconds = now.getSeconds();

        // Format waktu ke dalam format HH:mm:ss WIB
        var formattedTime = leadingZero(hours) + ":" + leadingZero(minutes) + ":" + leadingZero(seconds) + " WIB";

        return formattedTime;
      }

      // Fungsi untuk menambahkan nol di depan angka jika angka < 10
      function leadingZero(number) {
        return number < 10 ? "0" + number : number;
      }

      // Fungsi untuk memperbarui teks tombol dengan waktu saat ini
      function updateWaktu() {
        var waktuSekarang = getWaktuSekarang();
        document.getElementById("btnWaktu").innerHTML = "Waktu Sekarang<br><font size='4'>" + waktuSekarang + "</font>";
      }

      // Memperbarui waktu setiap detik
      setInterval(updateWaktu, 1000);

      // Memanggil fungsi updateWaktu() untuk pertama kali
      updateWaktu();
    </script>


